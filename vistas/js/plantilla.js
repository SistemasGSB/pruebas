$(document).on("click", ".btnEliminarProspeccion", function(){

  var idProspeccion = $(this).attr("idProspeccion");

  swal({
    title: '¿Está seguro de borrar la prospeccion?',
    text: "¡Si no lo está puede cancelar la accíón!",
    type: 'warning',
    showCancelButton: true,
    confirmButtonColor: '#3085d6',
      cancelButtonColor: '#d33',
      cancelButtonText: 'Cancelar',
      confirmButtonText: 'Si, borrar prospeccion!'
  }).then(function(result){

    if(result.value){

      window.location = "index.php?ruta=rep-prospeccion&idProspeccion="+idProspeccion;

    }

  })

});

